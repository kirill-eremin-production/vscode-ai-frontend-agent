/**
 * Константы и идентификаторы продактовой роли.
 *
 * Зачем отдельный файл:
 *  - имя модели и role-id используются в нескольких местах (сервис,
 *    resumer, loop.json), а захардкоженные литералы расползались бы
 *    по проекту;
 *  - смена модели = одно место + комментарий рядом, как требует
 *    AGENT.md ("Модели не выносим в settings, пока не нужно
 *    переключать без пересборки").
 *
 * Сам system prompt лежит в `product.prompt.ts` — он длинный и
 * шумит в этом файле, разделение упрощает чтение и code review
 * (правка prompt → diff в одном файле, правка модели → в другом).
 */

/**
 * Идентификатор роли. Пишется в `loop.json`, по нему resume-registry
 * находит resumer'а после перезапуска VS Code. Должен совпадать с
 * именем поддиректории kb (`KNOWLEDGE_SCHEMA.product`) — это даёт
 * естественный sandbox: каждая роль работает в своей kb-папке с
 * именем == role-id.
 */
export const PRODUCT_ROLE = 'product' as const;

/**
 * Модель для продакта.
 *
 * Выбор: `google/gemini-3.1-flash-lite-preview` — та же модель, что и
 * у smoke. Anthropic Claude Haiku возвращал HTTP 400 на первой итерации
 * из-за того, что tool names содержат точку (`kb.read`, ...): Anthropic
 * требует имена по `^[a-zA-Z0-9_-]+$`, а Gemini эту валидацию не делает.
 * Когда переименуем тулы (или добавим маппинг точек на стыке wire) —
 * можно будет вернуться на Haiku.
 */
export const PRODUCT_MODEL = 'google/gemini-3.1-flash-lite-preview';

/**
 * Тариф продактовой модели (USD за 1M токенов).
 *
 * Источник: OpenRouter тарифы `google/gemini-3.1-flash-lite-preview`
 * на момент релиза модели (2026-03-03). Литералом, как и сама модель —
 * AGENT.md фиксирует «модели не выносим в settings»; цены подчиняются
 * тому же правилу, чтобы их источник правды совпадал.
 *
 * Чтение: `pricingFor(PRODUCT_MODEL)` через `pricing/registry.ts` —
 * именно там ловится ситуация «модель неизвестна» и стоимость отдаётся
 * как `null` без падения рана.
 */
export const PRODUCT_PRICING = {
  /** Цена входных токенов за 1M. */
  inputPerMTok: 0.25,
  /** Цена выходных токенов за 1M. */
  outputPerMTok: 1.5,
} as const;

/**
 * Soft-limit контекста для индикатора заполненности (US-12).
 *
 * Жёсткий лимит модели — 1_048_576 (1M) токенов; но качество ответов
 * заметно деградирует уже после ~200k. Поэтому индикатор и кнопка
 * «Сжать контекст» (#0013) ориентируются на эту меньшую границу:
 * жёлтая зона — 60% от soft-limit, красная — 85%.
 *
 * Если в проде окажется, что модель тянет дольше — поднимем число
 * в одном месте, не трогая UI и формулы.
 */
export const PRODUCT_CONTEXT_LIMIT_TOKENS = 200_000;

