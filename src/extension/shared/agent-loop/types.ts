/**
 * Контракт тула для agent-loop'а.
 *
 * Тул — это пара «JSON Schema аргументов» + «handler, который их выполняет».
 * Agent-loop сам не знает, что делает конкретный тул: он валидирует входы
 * по schema, вызывает handler, упаковывает результат в `role: "tool"` и
 * возвращает модели на следующей итерации.
 *
 * Тулы регистрирует роль (продакт, архитектор и т.п.) — она же решает,
 * какое подмножество тулов давать модели на этом ране.
 */

/**
 * Один зарегистрированный тул. Дженерик — над типом распарсенных
 * аргументов; agent-loop работает с `ToolDefinition<unknown>` как с
 * базовым типом, а конкретные роли могут типизировать строже.
 */
export interface ToolDefinition<TArgs = unknown> {
  /**
   * Имя тула — то, что увидит модель в `tools[].function.name` и
   * чем будет ссылаться на него в `tool_calls`. Соглашение об именах:
   * `<пространство>.<действие>` (например, `kb.read`), чтобы можно
   * было давать роли «всё пространство `kb.*`» одной фильтрацией.
   */
  name: string;
  /**
   * Описание для модели — что тул делает и в каких случаях звать.
   * Должно быть достаточно конкретным, чтобы модель не путала тулы
   * со схожими именами.
   */
  description: string;
  /**
   * JSON Schema аргументов. На стороне модели — она же `parameters` в
   * tool definition. Минимум: `type: "object"` + `properties` + `required`.
   * Дополнительные свойства запрещаем (`additionalProperties: false`),
   * чтобы модель не «придумывала» лишние поля незаметно.
   */
  schema: Record<string, unknown>;
  /**
   * Handler — выполняет тул и возвращает результат. Может бросить —
   * agent-loop поймает и положит в `tool_result.error` для модели,
   * не уронит цикл целиком.
   *
   * Для `ask_user` — это особенный handler: вместо синхронного
   * результата он возвращает Promise, который резолвится после
   * ответа пользователя через IPC (см. Фазу B).
   */
  handler: (args: TArgs) => Promise<unknown>;
}

/**
 * Реестр доступных тулов внутри одного рана. Роль формирует свой
 * набор и передаёт в `runAgentLoop`. Используем `Map`, а не объект —
 * у Map предсказуемые semantics (нет prototype-pollution) и явное API.
 */
export type ToolRegistry = Map<string, ToolDefinition>;
