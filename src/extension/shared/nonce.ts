/**
 * Генератор криптографически НЕстрогого, но достаточного для CSP nonce.
 *
 * Зачем нужен nonce:
 * Content Security Policy webview-а требует, чтобы каждый <script> имел
 * атрибут `nonce`, совпадающий с тем, что объявлен в CSP-заголовке HTML.
 * Это защищает от инъекций сторонних скриптов: даже если злоумышленник
 * внедрит <script>, без правильного nonce браузер его не выполнит.
 *
 * Почему `Math.random`, а не `crypto.randomBytes`:
 * nonce должен быть лишь уникальным per-загрузка, а не криптостойким —
 * угрозы предсказать его у атакующего нет, потому что HTML с CSP и
 * nonce генерируются на стороне extension host и доходят до браузера
 * за один шаг. Math.random тут достаточно и не тянет лишних импортов.
 *
 * Почему 32 символа: подобрано как разумный минимум; при желании можно
 * увеличить — длина не влияет на корректность, только на «эстетику».
 */
export function getNonce(): string {
  // Алфавит — латиница + цифры. CSP не требует конкретного набора,
  // но безопаснее ограничиться ASCII, чтобы избежать сюрпризов с
  // экранированием в HTML-атрибуте.
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
  let text = '';
  for (let i = 0; i < 32; i++) {
    text += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return text;
}
