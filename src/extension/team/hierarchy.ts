/**
 * Иерархия ролей агентов команды.
 *
 * Один источник правды по порядку ролей: используется и canvas-layout
 * (#0028 → #0042), и runtime-проверкой эскалации (#0032 → #0038). Если
 * порядок продублировать в нескольких местах, рано или поздно списки
 * разъедутся — модуль ровно про это и существует.
 *
 * `User` в иерархию не входит осознанно: он не агент команды, а внешний
 * source of input. Цепочка эскалации между агентами строится только
 * по `HIERARCHY`.
 */

import type { KnowledgeRole } from '../entities/knowledge/schema';

/**
 * Тип роли в иерархии. Алиас существующего `KnowledgeRole`, чтобы не
 * заводить параллельный enum имён ролей: каждая новая kb-роль
 * автоматически становится участником иерархии (если попадает в
 * `HIERARCHY`-массив). По AC задачи #0033: тип `Role` берётся из
 * существующего места и `'user'` в него не входит.
 */
export type Role = KnowledgeRole;

/**
 * Упорядоченный список ролей от «самого продуктового» уровня к
 * «самому реализационному»: `product → architect → programmer`.
 * Соответствует направлению потока задач: продакт ставит ТЗ,
 * архитектор планирует, программист реализует.
 *
 * `readonly` — чтобы случайным `push` не сломать инварианты на
 * стороне потребителей.
 */
export const HIERARCHY: readonly Role[] = ['product', 'architect', 'programmer'];

/**
 * Индекс роли в `HIERARCHY`. Бросает на любой роли, которой в массиве
 * нет (включая ситуацию, когда строкой передали `'user'` в обход типа).
 *
 * Бросаем явной ошибкой вместо «вернуть -1 как у `indexOf`», чтобы баг
 * ловился в момент возникновения, а не маскировался невалидным
 * сравнением уровней дальше по потоку.
 */
export function levelOf(role: Role): number {
  const index = HIERARCHY.indexOf(role);
  if (index < 0) {
    throw new Error(`levelOf: роль "${role}" не входит в иерархию [${HIERARCHY.join(', ')}]`);
  }
  return index;
}

/**
 * Промежуточные роли строго между `a` и `b` в порядке цепочки.
 * Концы не включены. Для соседних ролей и для `a === b` — пустой массив.
 *
 * Порядок результата идёт от `a` к `b`: `rolesBetween('product',
 * 'programmer')` → `['architect']`, `rolesBetween('programmer',
 * 'product')` → `['architect']` (одна промежуточная, направление от
 * программиста к продакту).
 *
 * Зачем: правило эскалации (#0032/#0038) — если программист зовёт
 * продакта, в чат обязательно должен попасть архитектор. Эта функция
 * даёт список «кого ещё дотащить».
 */
export function rolesBetween(a: Role, b: Role): Role[] {
  const fromIndex = levelOf(a);
  const toIndex = levelOf(b);
  if (fromIndex === toIndex) return [];
  // Шаг направления: +1 если идём «вниз» по иерархии, -1 если «вверх».
  // Цикл идёт строго между концами (не включая их), поэтому стартуем
  // от соседа к стартовому уровню и останавливаемся ДО уровня b.
  const step = fromIndex < toIndex ? 1 : -1;
  const between: Role[] = [];
  for (let i = fromIndex + step; i !== toIndex; i += step) {
    between.push(HIERARCHY[i]);
  }
  return between;
}

/**
 * Соседние ли роли в иерархии (разница уровней ровно 1). Для одной и
 * той же роли — `false`: смысл «соседства» — наличие прямого канала
 * без промежуточных уровней; «сама с собой» этот сценарий не покрывает.
 */
export function areAdjacent(a: Role, b: Role): boolean {
  return Math.abs(levelOf(a) - levelOf(b)) === 1;
}
