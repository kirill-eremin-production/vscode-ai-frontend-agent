import { useEffect, useState } from 'react';

/**
 * Тип входящего сообщения от extension host.
 * Сейчас единственный кейс — `pong` в ответ на `ping`, но контракт
 * намеренно сделан как union, чтобы новые типы добавлялись через
 * расширение объединения, а не через изменение существующих полей.
 */
export type IncomingMessage = { type: 'pong'; at: number };

/**
 * Хук-журнал входящих сообщений из extension host.
 *
 * Слушает глобальное событие `message` на `window` (это единственный
 * способ для webview получить сообщения от хоста) и складывает каждое
 * полученное сообщение в локальный список как строку JSON.
 *
 * Почему храним именно строки, а не объекты:
 *  - текущее единственное потребление — отрисовка в `<pre>`, для неё
 *    JSON-строка уже готова к показу;
 *  - так проще диффать список в React (стабильные примитивы);
 *  - если позже понадобится структурированный доступ — поменяем тип
 *    хранилища и парсер, сигнатура хука останется простой.
 *
 * Возвращает массив строк в хронологическом порядке (старые сверху).
 */
export function useMessageLog(): string[] {
  const [log, setLog] = useState<string[]>([]);

  useEffect(() => {
    // Подписка на сообщения от extension host. Тип события сужаем
    // дженериком, чтобы TS подсказывал поля при дальнейших правках.
    const onMessage = (event: MessageEvent<IncomingMessage>) => {
      setLog((prev) => [...prev, JSON.stringify(event.data)]);
    };

    window.addEventListener('message', onMessage);

    // Снимаем подписку при размонтировании, иначе при HMR/повторном
    // монтировании накопятся дубликаты обработчиков.
    return () => window.removeEventListener('message', onMessage);
  }, []);

  return log;
}
